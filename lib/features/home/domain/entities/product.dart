class Product {
  final String id;
  final String name;
  final String description;
  final double price;
  final String imageUrl;
  final String category;
  final String subcategory;

  Product({
    this.id = '',
    this.name = '',
    this.description = '',
    this.price = 0.0,
    this.imageUrl = '',
    this.category = '',
    this.subcategory = '',
  });
}
